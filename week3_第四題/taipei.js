// 取得所有的 class 是 .image-item 元素（也就是 promotion1~3 和 Title1 ~ 10）
const items = document.querySelectorAll(".image-item");

// 遍歷資料並插入每一個項目
data.forEach((item, index) => {
  if (items[index]) {
    // 取得每個項目的圖片和文字區域
    const img = items[index].querySelector(".image"); // 將json的圖片塞到 class = image的地方 （也就是 promotion1~3 和 Title1 ~ 10）
    const textDiv = items[index].querySelector(".text"); // 將json的文字塞到 class = text的地方 （也就是 promotion1~3 和 Title1 ~ 10）

    // 更新圖片的 src 屬性
    img.src = item.image_url;

    // 更新文字內容
    textDiv.textContent = item.name;
  }
});

// 獲取 Load More 按鈕和容器元素
const loadMoreBtn = document.getElementById("load-more-btn");
const container = document.querySelector("body"); // 可以選擇其他容器，這裡選擇 body

// 目前顯示的資料索引，從上一輪 "Load More" 點擊後的最後索引開始
let currentIndex = items.length;

// 按下 Load More 按鈕時執行的函式
loadMoreBtn.addEventListener("click", function () {
  // 創建一個新的 big-and-mid-image-container 元素
  const newContainer = document.createElement("div");
  newContainer.classList.add("big-and-mid-image-container");

  // 設定新的項目數量（假設每次載入 10 個）
  const newItems = data.slice(currentIndex, currentIndex + 10); // 從 data 中切取下一批資料

  // 按照每 5 個顯示 5 個中圖和 3 個大圖來插入新內容
  newItems.forEach((item, index) => {
    const titleIndex = currentIndex + index + 1; // 計算標題的編號

    let itemHTML = "";
    // 根據索引決定是大圖還是中圖
    if (index % 5 === 0) {
      // index1與5是大圖
      itemHTML = `
          <div class="big-image-item image-item title-${titleIndex}">
            <img src="${item.image_url}" alt="Title ${titleIndex}" class="big-image image" />
            <div class="big-image-text text">${item.name}</div>
          </div>
    
        `;
    } else {
      // 除了1與5整除的index以外是中圖
      itemHTML = `
          <div class="mid-image-item image-item title-${titleIndex}">
            <img src="${item.image_url}" alt="Title ${titleIndex}" class="mid-image image" />
            <div class="mid-image-text text">${item.name}</div>
          </div>
    
        `;
    }

    // 將創建的 HTML 結構插入新的容器
    newContainer.innerHTML += itemHTML;
  });

  // 更新 currentIndex，確保下次點擊時是接續在已顯示的項目後面
  currentIndex += 10;

  // 把新的容器元素插入到頁面的最後
  container.appendChild(newContainer);

  // 按下按鈕會自動滾動到頁面底部
  window.scrollTo({
    top: document.body.scrollHeight,
    behavior: "smooth",
  });
});
